public class LocalVariables {
    public static void main(String[] args) {
        int x;
        if (args.length>0){
           x=10;
        }else{
           x=20;
        }
         System.out.println(x);
    }
}


//IN THE ABOVE CODE THE X IS LOCAL VARIABLE MEANS WHICH IS DECLEARDE INSIDE A METHOD
//LOCAL VARIABLES ARE DECLEARED IN SPECFIC PERPOSE
//THEY ARE NOT ACCESSED OUTSIDE THE METHOD