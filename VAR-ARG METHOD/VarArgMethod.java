public class VarArgMethod {
    public static void m1(){
     
        System.out.println("VAR-ARG METHOD EXECUTION");
    }
    public static void main(String[] args) {
        m1();
    }
}

// THIS IS VAR-ARG METHOD 
// WE CAN  DECLEARE A METHOD AND EXECUT IT AT THE TIME OOF REQUIRMENT
// THE METHOD IN JAVA SHOULD BE IMPLEMENTED IN MAIN METHOD ONLY